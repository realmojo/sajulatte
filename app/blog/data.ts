// 십성 계산 헬퍼
const getTenGod = (gan: string, ji: string) => {
  const ganEl = elements[gan as keyof typeof elements];
  const jiEl = elements[ji as keyof typeof elements];
  const order = ['목', '화', '토', '금', '수'];

  const ganIdx = order.indexOf(ganEl);
  const jiIdx = order.indexOf(jiEl);
  const diff = (jiIdx - ganIdx + 5) % 5;

  // 간단한 음양 계산 (0,2,4,6,8 양 / 1,3,5,7,9 음)
  const ganPol = stems.indexOf(gan) % 2;
  const jiPol = branches.indexOf(ji) % 2 === 0 ? 0 : 1; // 자(0)는 양, 축(1)은 음... (사실 자는 체는 양이나 용은 음.. 여기선 단순화)
  // * 지지 음양: 자(양), 축(음), 인(양), 묘(음)...
  // 실제 명리에서는 자수는 체양용음이나, 십성 계산시 본기 기준 수(水)로 계산.

  const isSamePol = ganPol === jiPol;

  // 0: 비겁, 1: 식상, 2: 재성, 3: 관성, 4: 인성
  if (diff === 0) return isSamePol ? '비견' : '겁재';
  if (diff === 1) return isSamePol ? '식신' : '상관';
  if (diff === 2) return isSamePol ? '편재' : '정재';
  if (diff === 3) return isSamePol ? '편관' : '정관';
  if (diff === 4) return isSamePol ? '편인' : '정인';
  return '비견';
};

